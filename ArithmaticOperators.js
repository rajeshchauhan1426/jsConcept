// javascript
// Copy code
// let remainder = 17 % 5; // remainder is 2 (because 17 divided by 5 is 3 with a remainder of 2)
// Exponentiation () (ES6)**: Raises the left operand to the power of the right operand.

// javascript
// Copy code
// let result = 2 ** 3; // result is 8 (2 raised to the power of 3)
// Increment (++): Increases the value of a variable by 1.

// javascript
// Copy code
// let count = 5;
// count++; // count is now 6

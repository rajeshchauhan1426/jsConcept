// Declaring strings
let singleQuoted = 'This is a string with single quotes.';
let doubleQuoted = "This is a string with double quotes.";

// Combining strings (concatenation)
let firstName = "John";
let lastName = "Doe";
let fullName = firstName + " " + lastName;
console.log(fullName); // Outputs: "John Doe"

// Escaping characters
let escapedString = "This is a \"quoted\" string with escape characters.";
console.log(escapedString); // Outputs: "This is a "quoted" string with escape characters."

// String length
let message = "Hello, world!";
console.log(message.length); // Outputs: 13

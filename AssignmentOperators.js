// In JavaScript, assignment operators are used to assign values to variables. They also allow you to perform an operation and assign the result to a variable in a single step. Here are some common assignment operators in JavaScript:

// Assignment Operator (=): The basic assignment operator is the equal sign (=). It assigns the value on the right to the variable on the left.

// javascript
// Copy code
// let x = 10;
// Addition Assignment Operator (+=): It adds the value on the right to the current value of the variable on the left and assigns the result back to the variable on the left.

// javascript
// Copy code
// let x = 5;
// x += 3; // x is now 8
// Subtraction Assignment Operator (-=): It subtracts the value on the right from the current value of the variable on the left and assigns the result back to the variable on the left.

// javascript
// Copy code
// let x = 10;
// x -= 2; // x is now 8
// Multiplication Assignment Operator (*=): It multiplies the value on the right by the current value of the variable on the left and assigns the result back to the variable on the left.

// javascript
// Copy code
// let x = 5;
// x *= 2; // x is now 10
// Division Assignment Operator (/=): It divides the current value of the variable on the left by the value on the right and assigns the result back to the variable on the left.

// javascript
// Copy code
// let x = 10;
// x /= 2; // x is now 5
// Modulus Assignment Operator (%=): It calculates the remainder when the current value of the variable on the left is divided by the value on the right and assigns the result back to the variable on the left.

// javascript
// Copy code
// let x = 7;
// x %= 3; // x is now 1
// Exponentiation Assignment Operator (=)**: It raises the variable on the left to the power of the value on the right and assigns the result back to the variable on the left. (Introduced in ECMAScript 2016)

// javascript
// Copy code
// let x = 2;
// x **= 3; // x is now 8
// Bitwise Assignment Operators: JavaScript also has bitwise assignment operators, such as &=, |=, and ^=, which perform bitwise operations and assign the result to the variable.

// javascript
// Copy code
// let x = 5; // binary: 0101
// x &= 3;    // x is now 1 (binary: 0001)
// These assignment operators allow you to perform operations and update variables efficiently in JavaScript.
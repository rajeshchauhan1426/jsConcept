let integerNumber = 42; // This is an integer.
let floatNumber = 3.14; // This is a floating-point number.

// Arithmetic operations
let sum = integerNumber + floatNumber; // Addition
let product = integerNumber * floatNumber; // Multiplication
let divisionResult = integerNumber / 2; // Division

console.log(sum); // Outputs: 45.14
console.log(product); // Outputs: 131.88
console.log(divisionResult); // Outputs: 21






//NewCode
let strNumber = "123.45";
let parsedNumber = parseFloat(strNumber);
console.log(parsedNumber); // Outputs: 123.45

// Using the Math object
let squared = Math.pow(5, 2); // Calculates 5 raised to the power of 2
let sqrt = Math.sqrt(16); // Calculates the square root of 16
console.log(squared); // Outputs: 25
console.log(sqrt); // Outputs: 4

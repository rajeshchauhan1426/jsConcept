// JavaScript comes with a wide range of built-in functions, often referred to as "built-in methods" or "native functions." These functions are part of the JavaScript language itself and are available for use without needing to define them. Here are some of the most commonly used built-in functions in JavaScript:

// 1. **String Methods:** JavaScript provides several methods for manipulating strings, such as `toUpperCase()`, `toLowerCase()`, `charAt()`, `substring()`, `split()`, `indexOf()`, and `replace()`.

// 2. **Array Methods:** Arrays have a variety of built-in methods, including `push()`, `pop()`, `shift()`, `unshift()`, `concat()`, `join()`, `slice()`, `splice()`, `forEach()`, `map()`, `filter()`, `reduce()`, and `sort()`.

// 3. **Math Functions:** The `Math` object provides mathematical functions like `Math.random()`, `Math.floor()`, `Math.ceil()`, `Math.round()`, `Math.max()`, and `Math.min()`.

// 4. **Date Methods:** The `Date` object has methods like `getYear()`, `getFullYear()`, `getMonth()`, `getDate()`, `getHours()`, `getMinutes()`, and `getTime()` for working with dates and times.

// 5. **RegExp Methods:** The `RegExp` object has methods like `test()`, `exec()`, and others for working with regular expressions.

// 6. **Global Functions:** JavaScript includes global functions like `parseInt()`, `parseFloat()`, `isNaN()`, `isFinite()`, `eval()`, and `setTimeout()` for various purposes.

// 7. **Type Conversion Functions:** Functions like `String()`, `Number()`, and `Boolean()` are used for type conversion.

// 8. **Object Methods:** Objects have built-in methods like `hasOwnProperty()`, `toString()`, and `valueOf()`.

// 9. **Console Methods:** For debugging and logging, JavaScript provides methods like `console.log()`, `console.error()`, and `console.warn()`.

// 10. **JSON Methods:** JavaScript has `JSON.stringify()` and `JSON.parse()` for working with JSON data.

// 11. **Promise Methods:** For handling asynchronous operations, Promises provide methods like `then()`, `catch()`, and `finally()`.

// 12. **ArrayBuffer and TypedArray Methods:** For working with binary data, JavaScript offers methods like those associated with `ArrayBuffer` and various TypedArray objects like `Int8Array`, `Uint8Array`, and others.

// 13. **Intl Object:** The `Intl` object provides internationalization and localization features, such as `Intl.DateTimeFormat` and `Intl.NumberFormat`.

// These are just a few examples of the many built-in functions and objects available in JavaScript. These built-ins provide essential functionality and can be combined creatively to accomplish a wide range of tasks in web development and other application areas.
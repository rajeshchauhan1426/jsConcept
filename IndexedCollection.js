// There are two main types of indexed collections in JavaScript: arrays and strings.

// Arrays:
// An array is a versatile and commonly used indexed collection. It's like a list of items, and each item is associated with a numeric index starting from 0 for the first item, 1 for the second item, and so on.

// Creating an array with some fruits
let fruits = ["apple", "banana", "orange", "grape"];

// Accessing elements using indexes
console.log(fruits[0]);  // Output: "apple"
console.log(fruits[2]);  // Output: "orange"
console.log(fruits[3]);  // Output: "grape"







// Strings:
// You might be surprised to know that strings are also indexed collections. Each character in a string can be accessed using an index just like in an array. The first character has index 0, the second character has index 1, and so on.

let message = "Hello, World!";
console.log(message[0]);  // Output: "H"
console.log(message[7]);  // Output: "W"
console.log(message[12]); // Output: "!"
